<script>
  let indiceProyecto = 0;

  function obtenerProyectosParaFrontEnd() {
    $.ajax({
      type: "POST",
      url: "?controlador=Proyectos&accion=obtenerProyectos",
      dataType: "json",
      success: function (response) {
        console.log(response);
        let carrusel = $(".carrusel_proyectos");
        carrusel.empty();

        response.forEach(proyecto => {
          // Extraer solo las URLs de imagenes
          let urlsImagenes = proyecto.imagenes.map(img => img.url);

          // Verificamos que al menos tenga una imagen
          let imagenPrincipal = urlsImagenes.length > 0 ? urlsImagenes[0] : "public/assets/sin_imagen.jpg";

          let proyectoHTML = `
                    <div class="proyecto">
                        <img src="${imagenPrincipal}" alt="${proyecto.nombre}">
                        <div class="info_proyecto">
                            <div class="info_texto">
                                <h3>${proyecto.nombre}</h3>
                                <p>${proyecto.descripcion}</p>
                            </div>
                            <button 
                                class="ver_mas" 
                                data-imagenes='${JSON.stringify(urlsImagenes)}' 
                                data-nombre='${JSON.stringify(proyecto.nombre)}' 
                                data-descripcion='${JSON.stringify(proyecto.descripcion)}'
                            >
                                Ver más
                            </button>
                        </div>
                    </div>
                `;
          carrusel.append(proyectoHTML);
        });

        mostrarProyecto(indiceProyecto);

        $(".ver_mas").click(function () {
          let imagenes = JSON.parse($(this).attr("data-imagenes"));
          let nombre = JSON.parse($(this).attr("data-nombre"));
          let descripcion = JSON.parse($(this).attr("data-descripcion"));

          let modalCarrusel = $(".imagenes_modal");
          modalCarrusel.empty();

          imagenes.forEach((img, i) => {
            modalCarrusel.append(`<img src="${img}" style="display: ${i === 0 ? 'block' : 'none'};">`);
          });

          $(".nombre_proyecto_modal").text(nombre);
          $(".descripcion_proyecto_modal").text(descripcion);
          $("#modal_proyecto").fadeIn();
        });
      },
      error: function (xhr, status, error) {
        console.log(error, xhr, status);
      }
    });
  }

  function mostrarProyecto(indice) {
    let proyectos = $(".proyecto");
    proyectos.hide();
    $(proyectos[indice]).show();
  }

  $(".prev_proyecto").click(function () {
    let proyectos = $(".proyecto");
    indiceProyecto = indiceProyecto > 0 ? indiceProyecto - 1 : proyectos.length - 1;
    mostrarProyecto(indiceProyecto);
  });

  $(".next_proyecto").click(function () {
    let proyectos = $(".proyecto");
    indiceProyecto = indiceProyecto < proyectos.length - 1 ? indiceProyecto + 1 : 0;
    mostrarProyecto(indiceProyecto);
  });

  $(".close").click(function () {
    $("#modal_proyecto").fadeOut();
  });

  $(".prev_img_modal").click(function () {
    let actual = $(".imagenes_modal img:visible");
    let anterior = actual.prev("img");
    if (anterior.length) {
      actual.hide();
      anterior.show();
    }
  });

  $(".next_img_modal").click(function () {
    let actual = $(".imagenes_modal img:visible");
    let siguiente = actual.next("img");
    if (siguiente.length) {
      actual.hide();
      siguiente.show();
    }
  });

  obtenerProyectosParaFrontEnd();
</script>

